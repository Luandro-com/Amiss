<p>
The sudden suicide of Anna Choi, a young Korean woman, forces her grief-stricken father, a powerful, corrupt man, to seek retribution against those responsible for pushing her over the edge. He seeks out the ones with whom she was closely involved with in the short time leading up to her death: her boyfriend, her psychologist and a stranger who claims not to have known her. With the help of his trusted henchman, he has them captured and tied up in an abandoned warehouse with the intention of interrogating them to find out the truth behind his daughter’s suicide and killing the one he feels is to blame. In the middle of nowhere, far away from help of any kind, they have no choice but to co-operate with the father’s will, while trying to find a way out. Through a series of flashbacks, we discover who all these people really are and the role they played in Anna’s life. However, nothing is as it seems and the truth is shrouded in darkness, for one of them is lying. At the end, the revelation behind Anna’s death is more shocking and sad than anyone could have imagined.
</p>
<p>
'Amiss' is an original mystery suspense-thriller, written and directed by Raoul Dyssell and William Sonbuchner. The film deals with the frightening reality of the human being in a desperate situation: you have no idea who you are.
